'''
An integer is read from input into variable num_inches. Use a conversion function to convert the value of num_inches to a string and assign the value to the variable str_inches.

'''
# Input: Read an integer from the user
num_inches = int(input())

# Convert the integer to a string using the str() function
str_inches = str(num_inches)

# Output the result (optional)
print(f'The string value of num_inches is: {str_inches}')